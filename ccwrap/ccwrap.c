struct command_and_terminator {
	struct args args;
	_Bool ends_with_pipe;
};
static _Bool iterate_commands(struct command_and_terminator *commands, unsigned ncommands,
	_Bool (*cb)(struct command_and_terminator *, void *), void *arg)
{
	for (unsigned i_cmd = 0; i_cmd < ncommands; ++i_cmd)
	{
		_Bool carry_on = cb(&commands[i_cmd], arg);
		if (!carry_on) return 0;
	}
	return 1;
}

static void print_command_args(FILE *stream, struct args *args)
{
	for (unsigned i_word = 0; i_word < args->argc; ++i_word)
	{
		if (i_word != 0) fprintf(stderr, " ");
		// FIXME: quote shell, protect from word-splitting
		fprintf(stream, "%s", args->argv[i_word]);
	}
}
static _Bool print_command(struct command_and_terminator *cmd, void *stream_as_void)
{
	FILE *stream = stream_as_void;
	fprintf(stream, " ");
	print_command_args(stream, &cmd->args);
	if (cmd->ends_with_pipe)
	{
		fprintf(stream, " |");
	}
	fprintf(stream, "\n");

	return 1;
}

static int input_fd = -1;
static _Bool rewrite_and_run_command(struct command_and_terminator *cmd, void *driver_pathname_as_void)
{
	// debugging
	if (debug_level >= 1) print_command(cmd, debug_stream?:stderr);
	// now the real stuff
	char *driver_pathname = driver_pathname_as_void;
	struct args the_cmd;
	debug_println(1, "basename is %s", basename(cmd->args.argv[0]));
	if (EQUAL_ANY(basename(cmd->args.argv[0]), "cc1", "cc1plus", "clang")
		&& cc1_command_is_pp(&cmd->args))
	{
		char *driver_args[] = { driver_pathname, "-E", NULL };
		struct args pp_cmd_args = get_cc_options_from_cc1_command(&cmd->args, driver_pathname);
		the_cmd = (struct args) {
			.argv = realloca_argv_with_prefix(pp_cmd_args.argc - 1, pp_cmd_args.argv + 1, 2, driver_args),
			.argc = pp_cmd_args.argc - 1 + 2
		};
	}
	else if (EQUAL_ANY(basename(cmd->args.argv[0]), "cc1", "cc1plus", "clang"))
	{
		// non-pp case
		char *driver_args[] = { driver_pathname, "-S", NULL };
		struct args non_pp_cmd_args = get_cc_options_from_cc1_command(&cmd->args, driver_pathname);
		the_cmd = (struct args) {
			.argv = realloca_argv_with_prefix(non_pp_cmd_args.argc - 1, non_pp_cmd_args.argv + 1, 2, driver_args),
			.argc = non_pp_cmd_args.argc - 1 + 2
		};
	}
	else
	{
		the_cmd = cmd->args;
	}
	if (debug_level >= 1)
	{
		fprintf(debug_stream?:stderr, "BECOMES... ");
		print_command_args(debug_stream?:stderr, &the_cmd);
		fprintf(debug_stream?:stderr, "\n");
	}
	int maybe_our_input = input_fd;
	int maybe_our_output;
	/* We may have to pipe the output onwards */
	int the_pipe[2] = { -1, -1 };
	if (cmd->ends_with_pipe)
	{
		/* open a pipe */
		int ret = pipe(the_pipe);
		if (ret != 0) err(EXIT_FAILURE, "opening pipe");
		// leave the read end for the next command around the loop
		maybe_our_output = the_pipe[1];
		input_fd = the_pipe[0];
	}
	else
	{
		maybe_our_output = -1;
		input_fd = -1; /* what we leave for the next iteration;
			we've snarfed *our* input fd, if any, as 'maybe_our_input' */
	}
	/* We should now have our command to run. We may have an fd
	 * to read from as stdin, or not, and one to write as stdout, or not. */
	pid_t p = fork();
	if (p == -1) err(EXIT_FAILURE, "forking");
	if (p == 0)
	{
		/* we are the child */
		int ret;
		if (maybe_our_input != -1)  { ret = dup2(maybe_our_input, 0);  if (ret == -1) err(EXIT_FAILURE, "input dup");  }
		assert(maybe_our_output != 1);
		if (maybe_our_output != -1) { ret = dup2(maybe_our_output, 1); if (ret == -1) err(EXIT_FAILURE, "output dup from %d (pid %d, about to exec %s)", maybe_our_output, (int) getpid(), the_cmd.argv[0]); }
		ret = (strchr(the_cmd.argv[0], '/') ? execve : execvpe)(
				the_cmd.argv[0], the_cmd.argv, environ
			);
		err(EXIT_FAILURE, "execve() returned %d", ret);
	}
	else
	{
		/* we are the parent; we have no need for those pipe ends now, so
		 * we'd like to close them, but if we do, our input dup fails. why?
		 * The child's copy of the file descriptor should be distinct.
		 * For now just close the writing end. That seems to be OK. */
		if (the_pipe[0] != -1) 
		{
			assert(the_pipe[1] != -1);
			//close(the_pipe[0]);
			close(the_pipe[1]);
		} else assert(the_pipe[1] == -1);
		/* do we wait for this one or keep going? */
		if (!cmd->ends_with_pipe)
		{
			int wstatus;
			pid_t w;
			do {
				w = waitpid(p, &wstatus, WUNTRACED | WCONTINUED);
				if (w == -1) {
					err(EXIT_FAILURE, "waitpid");
				}
				if (WIFEXITED(wstatus)) {
					debug_println(1, "child exited, status %d", (int) WEXITSTATUS(wstatus));
				} else if (WIFSIGNALED(wstatus)) {
					debug_println(0, "child killed by signal %d", (int) WTERMSIG(wstatus));
				}
			} while (!WIFEXITED(wstatus) && !WIFSIGNALED(wstatus));
			if (WIFSIGNALED(wstatus)) exit(2);
			if (0 != WEXITSTATUS(wstatus)) exit(1);
		}
		else
		{
			// just continue... we may need to fork a command that consumes the output
		}
	}
	return 1; // keep going
}

int main(int argc, char **argv)
{
	char *debug_var;
	if (NULL != (debug_var = getenv("DEBUG_CC")))
	{
		debug_level = atoi(debug_var);
		debug_stream = stderr;
	}
	int exit_status; // no init; compiler will warn if we use uninit'd along any path
	if (argc < 2) die("must give a compiler command to wrap");
	char *driver_arg = argv[1]; // chomp the driver name

	unsigned driver_argv_count = 1;
	char **driver_argv_strings = malloc(sizeof (char**));
	if (!driver_argv_strings) die("could not allocate memory (argv vec)");
	driver_argv_strings[0] = driver_arg; // we may append more

	char *driver_pathname = NULL;
	if (strchr(driver_arg, '/')) driver_pathname = driver_arg;
	else
	{
		/* The driver is not named by an absolute (/...) or CWD-relative (e.g. ./) path,
		 * but simply by something that needs PATH-based resolution. We have to do this
		 * ourselves, but we cheat: use 'which' in the '//' line at the very top of the
		 * file. So we should never reach this. */
		errx(EXIT_FAILURE, "internal error: driver command is not a pathname (see 'which' on top line)");
	}
	char *driver_realpath = realpath(driver_pathname, NULL);
	if (!driver_realpath) die("driver does not exist?!");
	char *driver_realpath_basename = basename(driver_realpath);
	char *found;
	if (NULL != (found = strstr(driver_realpath_basename, "gcc")))
	{
		APPEND3(driver_argv_count, driver_argv_strings, "-no-integrated-cpp");
	}
	else if (NULL != (found = strstr(driver_realpath_basename, "clang")))
	{
		APPEND3(driver_argv_count, driver_argv_strings, "-traditional-cpp");
		// FIXME: only set not_really_traditional if -traditional-cpp is not present in args
		not_really_traditional = 1; // we will skip -traditional-cpp when writing cc args
	}
	APPEND3(driver_argv_count, driver_argv_strings, "-###");
	free(driver_realpath);
	driver_realpath = NULL;

	// get the subcommand lines that the driver wants to run
	struct args prefixed = {
		.argv = realloca_argv_with_prefix(argc - 2, argv + 2, driver_argv_count, driver_argv_strings),
		.argc = argc - 1 + driver_argv_count
	};
	FILE *lines = popen_argv_for_reading(prefixed.argc, prefixed.argv);
	ssize_t nread;
	size_t len = 0;
	char *the_line_allocated = NULL;
	struct command_and_terminator *commands = NULL; // an array of commands, initially 0 of them
	unsigned commands_count = 0;
	do
	{
		len = 0;
		/* PROBLEM: getline() may hang, e.g. if the child process is defunct.
		 * Actually, is that the problem? If I hack in the 'wait' below, then
		 * the defunct child process successfully exits, but getline() still
		 * hangs. */
		nread = getline(&the_line_allocated, &len, lines);
		debug_println(1, "nread was %d", (int) nread);
		assert(the_line_allocated);
		if (nread != -1)
		{
			/* we have got a line; we care only about those beginning with ' ' */
			if (!(nread >= 1 && the_line_allocated[0] == ' ')) goto next;
			/* we have got a line beginning with ' '... it is shell-quoted */
			commands = realloc(commands, ++commands_count * sizeof *commands);
			if (!commands) die("could not allocate memory (commands vector)");
			struct command_and_terminator *the_command = &commands[commands_count - 1];
			*the_command = (struct command_and_terminator) {
				.args = parse_shell_quoted_line_as_argv(the_line_allocated + 1 /* skip the space*/)
			};
			if (0 == strcmp(the_command->args.argv[the_command->args.argc - 1], "|"))
			{
				the_command->ends_with_pipe = 1;
				/* Drop the literal pipe token */
				the_command->args.argv[the_command->args.argc - 1] = NULL;
				the_command->args.argc--;
			}
		}
	next:
		free(the_line_allocated);
		the_line_allocated = NULL;
	} while (nread != -1 && nread != 0);
	pclose_argv(lines);

	/* We've read some commands; we should rewrite them. What's our strategy for that?
	 * Each command may be something we know how to
	 * scan              (chunk up the options; with CC_IDENTIFY_ARGS)
	 * rewrite-and-lift  (e.g. cilpp: $driver -E to a tempfile, then CIL to the intended output file)
	 *      -- above we do this by normalize_and_lift stuff
	 * just lift?
	 *
	 * RE-READ my blog post before going further. Re-read 'wrapper' also. */
	
	/* Then do we print them or run them? Do a linear scan of the driver options
	 * looking for '-###'. If we see it, print then exit. If we get to the end
	 * without seeing it, run them.
	 *
	 * XXX: we should only run them after doing a "default rewriting", which,
	 * for commands we recognise, means to lift them to single-function driver invocations.
	 * Use old-style lifting for now. If that results in a wrapper that performs well-ish,
	 * then it will be worth hte extra effort to .c-ify the clang/gcc-probing stuff.
	 * We already have the lifting code above!!!! So this should be easy. */
	exit_status = 0;
	for (unsigned i_driver_arg = 0; i_driver_arg < argc; ++i_driver_arg)
	{
		if (0 == strcmp(argv[i_driver_arg], "-###"))
		{
			iterate_commands(commands, commands_count, print_command, NULL);
			goto out;
		}
	}
	/* If we got here, we want to run the commands. We do a "default rewrite"
	 * for any that we recognise. The default rewrite is to lift to a single-function
	 * driver invocation. */
	exit_status = 1;
	_Bool success = iterate_commands(commands, commands_count, rewrite_and_run_command, driver_pathname);
	if (success) exit_status = 0;
out:
	return exit_status;
}
