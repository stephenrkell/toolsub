struct normalization_result
{
	char **saw_E;
	char **saw_Os;
	char **saw_Werror;
	char **saw_D_FORTIFY_SOURCE;
	char **saw_o;
	unsigned opos_D_FORTIFY_SOURCE;
	unsigned infiles_count;
	char **infiles_names;
};
// We run a normalizing pass on a cc1 command, to remove some awkward forms from it.
// NOTE: normalizing cc1 options does not remove the nasty "-MD <argument>" form.
//
// What does it do?
// It tries to enumerate input files, but is very imprecise at present.
// It identifies the output file, pulling out "-o" and then re-adding it (pointlessly).
//
// The main purpose is to rewrite cases where a single cc1 invocation works, but
// splitting into separate preprocessing and compilation invocations doesn't.
// The main thing currently is to futz with options to avoid a breaking interaction
// between -Werror, -D_FORTIFY_SOURCE and -Os.
struct args normalize_cc1_argv(struct args *in, struct normalization_result *res)
{
	struct args out = { .argc = 0, .argv = NULL };
	/* our shell loop iterated over $1.., where $0 was the cc1 command, and
	 * wrote output infiles[0..] and norm_cc1_options[0..]
	 * but this was immediately corrected in its caller by prepending the cc1 command.
	 * We instead copy the cc1 command. */
	assert(in->argc > 0);
	APPEND(out, in->argv[0]);
	for (unsigned i = 1; i < in->argc; ++i)
	{
		debug_println(2, "args remaining %d, argv[i] is `%s'", in->argc - i, in->argv[i]);
		if (EQUAL(in->argv[i], "-o"))
		{
			if (i+1 == in->argc) { die("refusing to process argvec ending with `-o'"); }
			res->saw_o = &in->argv[i];
			++i; // skip one
			continue; // we do not snarf this; caller must inspect saw_o
		}
		if (EQUAL(in->argv[i], "-E"))
		{
			res->saw_E = &in->argv[i];
			goto snarf;
		}
		if (EQUAL(in->argv[i], "-Os"))
		{
			res->saw_Os = &in->argv[i];
			goto snarf;
		}
		if (EQUAL(in->argv[i], "-Werror"))
		{
			res->saw_Werror = &in->argv[i];
			goto snarf;
		}
		if (EQUAL(in->argv[i], "-D_FORTIFY_SOURCE") || HAS_PREFIX(in->argv[i], "-D_FORTIFY_SOURCE="))
		{
			res->saw_D_FORTIFY_SOURCE = &in->argv[i];
			res->opos_D_FORTIFY_SOURCE = out.argc;
			goto snarf;
		}
		if (EQUAL(in->argv[i-1], "-D") && (
			EQUAL(in->argv[i], "_FORTIFY_SOURCE") || HAS_PREFIX(in->argv[i], "_FORTIFY_SOURCE=")
		))
		{
			res->opos_D_FORTIFY_SOURCE = out.argc - 1;
			goto snarf;
		}
		if (EQUAL(in->argv[i], "-"))
		{
			APPEND3(res->infiles_count, res->infiles_names, "-");
			goto snarf;
		}
		snarf:
		if (in->argv[i][0] != '-')
		{
			// FIXME: this is super-imprecise.
			// A useful regex for scraping the gcc man page for opts-with-args:
			// -[-a-z0-9]+ [^-[:blank:]]
			if (EQUAL_ANY(in->argv[i-1], "-D", "-U", "-x", "-aux-info", "-dumpbase", "-auxbase",
				"-I", "-u", "-T", "--param", "-G", "-MF", "-MD", "-MMD")
			|| HAS_PREFIX(in->argv[i-1], "-X")
			|| (HAS_PREFIX(in->argv[i-1], "-i") && HAS_PREFIX_ANY(in->argv[i-1], "-ia", "-ib", "-ic", "-id", "-ie", "-if", "-ig", "-ih", "-ii", "-ij", "-ik", "-il", "-im", "-in", "-io", "-ip", "-iq", "-ir", "-is", "-it", "-iu", "-iv", "-iw", "-ix", "-iy", "-iz"))
			)
			{
				// it's the option's argument
			}
			else
			{
				// it may be an input file
				APPEND3(res->infiles_count, res->infiles_names, in->argv[i]);
			}
		}
		debug_println(2, "Normalize: snarfing %s", in->argv[i]);
		APPEND(out, in->argv[i]);
	} // end for i
	// re-add -o
	if (res->saw_o)
	{
		APPEND(out, "-o");
		APPEND(out, *(res->saw_o + 1));
	}
	// if we saw -Os and -Werror and -D_FORTIFY_SOURCE, then we have a problem:
	// -Os adds a builtin #define of _FORTIFY_SOURCE which triggers a multiple
	// definition warning, hence error. The integrated cpp avoids this, presumably
	// by detecting the already-present -D_FORTIFY_SOURCE. Arguably the client
	// is at fault, although it's not documented that -Os implies -D_FORTIFY_SOURCE.
	// HACK: if we saw all three, change -D_FORTIFY_SOURCE to -U_...
	if (res->saw_Os && res->saw_Werror && res->saw_D_FORTIFY_SOURCE)
	{
		// another HACK: to handle separate "-D" and "_FORTIFY_SOURCE", we only assume that
		// the pos records where the "-D" is
		if (EQUAL(out.argv[res->opos_D_FORTIFY_SOURCE], "-D"))
		{
			out.argv[res->opos_D_FORTIFY_SOURCE] = "-U";
			// rewrite the next one too
			char *found;
			if (NULL != (found = strchr(out.argv[res->opos_D_FORTIFY_SOURCE+1], '=')))
			{
				*found = '\0'; // HACK: here we in-place-update the input string
			}
		}
		else if (HAS_PREFIX(out.argv[res->opos_D_FORTIFY_SOURCE], "-D_FORTIFY_SOURCE"))
		{
			// HACK: here we in-place-update the input string *twice*
			out.argv[res->opos_D_FORTIFY_SOURCE][1] = 'U'; // change -D... to -U...
			char *found;
			if (NULL != (found = strchr(out.argv[res->opos_D_FORTIFY_SOURCE], '=')))
			{
				*found = '\0';
			}
		}
	}
	return out;
}

static void write_args(FILE *stream, struct args *args)
{
	for (int i = 0; i < args->argc; ++i)
	{
		fprintf(stream, "%s%s", (i != 0) ? " " : "", args->argv[i]);
	}
	fprintf(stream, "\n");
}
struct args lift_normalized_cc1_to_cc(struct args *in, char *driver, struct normalization_result *res)
{
	//debug_print 1 "writing cc options for $@" 1>&2
	if (debug_level >= 1)
	{
		fprintf(debug_stream?:stderr, "writing cc options for ");
		write_args(debug_stream?:stderr, in);
	}
	struct args out = { .argc = 0 };
	APPEND(out, driver);
	for (unsigned i = 1; i < in->argc; ++i)
	{
		debug_println(2, "args remaining %d, argv[i] is `%s'", in->argc - i, in->argv[i]);
		// we are running our own .i-to-.s pass -- filter out cpp-only args
		if (EQUAL_ANY(in->argv[i], "-imultiarch", "-triple", "-main-file-name", "-ferror-limit", "-target-cpu", "-tune-cpu", "-mrelocation-model")
				|| HAS_PREFIX_ANY(in->argv[i], "-iremap", "-internal-"))
		{
			// skip the arg too
			++i;
		}
		else if (EQUAL_ANY(in->argv[i], "-quiet", "-fhonour-copts", "-mframe-pointer=all",
				"-mconstructor-aliases", "-munwind-tables", "-disable-free", "-disable-llvm-verifier",
				"-discard-value-names")
			||  HAS_PREFIX(in->argv[i], "-debugger-tuning="))
		{
			// skip just it
			continue;
		}
		else if (EQUAL(in->argv[i], "-traditional-cpp"))
		{
			if (not_really_traditional) continue;
			debug_println(0, "Really traditional!");
		}
		else if (EQUAL(in->argv[i], "-cc1"))
		{
			continue; // we are clang, but we want to write a cc command
		}
		else if (EQUAL_ANY(in->argv[i], "-MD", "-MMD"))
		{
			// NASTY: with cc1, -MD has an argument! it's the filename, like with -MF.
			// with cc or cpp, we have to use -MM?D -MF
			APPEND(out, in->argv[i]);
			APPEND(out, "-MF");
			if (i+1 >= in->argc) die("-MD or -MMD as last argument");
			APPEND(out, in->argv[i+1]); // the filename argument
		}
		else
		{
			debug_println(2, "Write-cc: snarfing %s", in->argv[i]);
			APPEND(out, in->argv[i]);
		}
	}
	return out;
}

